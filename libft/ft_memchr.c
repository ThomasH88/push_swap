#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*ptr;

	ptr = (unsigned char *)s;
	while (n > 0)
	{
		if ((unsigned char)c == (unsigned char)*ptr)
			return ((void *)s);
		s++;
		ptr++;
		n--;
	}
	return (NULL);
}
